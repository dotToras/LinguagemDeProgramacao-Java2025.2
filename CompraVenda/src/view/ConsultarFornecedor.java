    public void preencherTabela() {
        FornecedorDAO fDAO = new FornecedorDAO();
        
        List<Fornecedor> listaFornecedores = fDAO.consultarFornecedoressEndereco();
        
        DefaultTableModel tabelaFornecedores = (DefaultTableModel) tblFornecedores.getModel();
        
  
        for(Fornecedor forn : listaFornecedores) {
            Object[] obj = new Object[]{
                forn.getCodigo(),
                forn.getNome(),
                forn.getNomeFantasia(),
                forn.getCNPJ(),
                forn.getTelefone(),
                forn.getEmail(),
                forn.getEndereco().getEnderecoRua(),
                forn.getEndereco().getEnderecoBairro(),
                forn.getEndereco().getEnderecoComplemento(),
                forn.getEndereco().getEnderecoNumero(),
                forn.getEndereco().getEnderecoCEP(),
                forn.getEndereco().getEnderecoCidade(),
                forn.getEndereco().getEnderecoEstado(),
                forn.getEndereco().getEnderecoUF()
         };
            tabelaFornecedores.addRow(obj);
               
        }
    }
