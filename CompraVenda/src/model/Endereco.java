
package model;

/**
 * classe criada para representar os Dados de um Endereço
 * @author Matheus Ferreira Gonçalves
 * @since classe criada em Oct 10, 2025
 */
public class Endereco {

    int enderecoCodigo;
    String enderecoEstado;
    String enderecoCidade;
    String enderecoBairro;
    String enderecoRua;
    String enderecoUF;
    String enderecoComplemento;
    int enderecoNumero;
    String enderecoCEP;

    
    
    public int getEnderecoCodigo() {
        return enderecoCodigo;
    }

    public void setEnderecoCodigo(int enderecoCodigo) {
        this.enderecoCodigo = enderecoCodigo;
    }

    public String getEnderecoEstado() {
        return enderecoEstado;
    }

    public void setEnderecoEstado(String enderecoEstado) {
        this.enderecoEstado = enderecoEstado;
    }

    public String getEnderecoCidade() {
        return enderecoCidade;
    }

    public void setEnderecoCidade(String enderecoCidade) {
        this.enderecoCidade = enderecoCidade;
    }

    public String getEnderecoBairro() {
        return enderecoBairro;
    }

    public void setEnderecoBairro(String enderecoBairro) {
        this.enderecoBairro = enderecoBairro;
    }

    public String getEnderecoRua() {
        return enderecoRua;
    }

    public void setEnderecoRua(String enderecoRua) {
        this.enderecoRua = enderecoRua;
    }

    public String getEnderecoUF() {
        return enderecoUF;
    }

    public void setEnderecoUF(String enderecoUF) {
        this.enderecoUF = enderecoUF;
    }

    public String getEnderecoComplemento() {
        return enderecoComplemento;
    }

    public void setEnderecoComplemento(String enderecoComplemento) {
        this.enderecoComplemento = enderecoComplemento;
    }

    public int getEnderecoNumero() {
        return enderecoNumero;
    }

    public void setEnderecoNumero(int enderecoNumero) {
        this.enderecoNumero = enderecoNumero;
    }

    public String getEnderecoCEP() {
        return enderecoCEP;
    }

    public void setEnderecoCEP(String enderecoCEP) {
        this.enderecoCEP = enderecoCEP;
    }
    
    
    
} // fim da classe
