    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        alunoDAO aDAO = new alunoDAO();
        
        long RA =  Long.parseLong(txtRA.getText());
        String nome = txtNome.getText();
        String curso = txtCurso.getText();
        int semestre = Integer.parseInt(txtSemestre.getText());
        Aluno aluno = new Aluno(RA, nome, curso, semestre);
        
        aDAO.inserirAluno(aluno);
        
        limparFormulario();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        
        long RA = Long.parseLong(txtRAPesquisa.getText());
        alunoDAO aDAO = new alunoDAO();
        
        Aluno aluno = aDAO.buscarAlunoRA(RA);
        
        if(aluno == null){
            JOptionPane.showMessageDialog(null, "Aluno n√£o encontrado !!!");
            limparFormulario();
        }
        else
        {
            txtRAretorno.setText(String.valueOf(aluno.codigo));
            txtNomeConsulta.setText(aluno.nome);
            txtCurso1.setText(aluno.curso);
            txtSemestrePesquisa.setText(String.valueOf(aluno.semestre));
            
        }
        
    }//GEN-LAST:event_btnPesquisarActionPerformed

