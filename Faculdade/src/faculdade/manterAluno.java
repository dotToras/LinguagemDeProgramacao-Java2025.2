    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
        
        if(resposta == JOptionPane.YES_OPTION){
            alunoDAO aDAO = new alunoDAO();
            long RA = Long.parseLong(txtRAPesquisa.getText().trim());
            aDAO.excluirAluno(RA);
            limparFormulario();
        }

    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        alunoDAO aDAO = new alunoDAO();
        
        Aluno aluno = new Aluno();
        
        aluno.codigo = Long.parseLong(txtRAPesquisa.getText());
        aluno.nome = txtNomeConsulta.getText();
        aluno.curso = txtCurso1.getText();
        aluno.semestre = Integer.parseInt(txtSemestrePesquisa.getText());
        
        aDAO.editarAluno(aluno);
        limparFormulario();
    }//GEN-LAST:event_btnAtualizarActionPerformed

