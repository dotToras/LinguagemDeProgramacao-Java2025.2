    private void brnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnInserirActionPerformed
        Pessoa pessoa = new Pessoa();
        
        String sexo = "";
        
        if(rbn_Feminino.isSelected()){
            sexo = "Feminino";
        }
        else if(rbn_Masculino.isSelected()){
            sexo = "Masculino";
        }
                
        pessoa.setNome(txtNome.getText());
        pessoa.setIdioma((String)cbb_Idiomas.getSelectedItem());
        pessoa.setSexo(sexo);
        
        
        PessoaDAO pDAO = new PessoaDAO();
        pDAO.inserirPessoa(pessoa);
       
        limparFormulario();
    }//GEN-LAST:event_brnInserirActionPerformed

    private void limparFormulario(){
        txtNome.setText("");
        btg_Sexo.clearSelection();
        cbb_Idiomas.setSelectedItem(0);
    }
